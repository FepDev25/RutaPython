"""
Características clave de Python:
1. Sintaxis Limpia y Legible: Python se enorgullece de su sintaxis clara y legible que se asemeja al lenguaje humano. 
Esto facilita la lectura y el mantenimiento del código.

Tipado Dinámico: Python es un lenguaje de tipado dinámico, lo que significa que no necesitas declarar explícitamente el tipo de una
variable. El tipo de variable se determina en tiempo de ejecución.

Interpretado: Python es un lenguaje interpretado, lo que significa que no es necesario compilar el código antes de ejecutarlo. 
Esto agiliza el proceso de desarrollo y prueba.

Amplia Biblioteca Estándar: Python tiene una biblioteca estándar rica y diversa que proporciona módulos y paquetes para realizar 
una variedad de tareas, desde operaciones de archivo hasta desarrollo web.

Orientado a Objetos: Python es un lenguaje orientado a objetos, lo que significa que todo en Python es un objeto. Esto promueve 
la reutilización de código y una estructura más organizada.

Multiplataforma: Python es compatible con múltiples plataformas, lo que significa que puedes escribir código en una plataforma y 
ejecutarlo en otra sin muchos cambios.

Comunidad Activa: La comunidad de Python es grande y activa, lo que significa que hay una gran cantidad de recursos en línea, 
bibliotecas de terceros y soporte disponible.

Aplicaciones Diversas: Python se utiliza en diversas áreas, como desarrollo web (con frameworks como Django y Flask), análisis de 
datos (con bibliotecas como Pandas y NumPy), inteligencia artificial y aprendizaje automático (con bibliotecas como TensorFlow y
scikit-learn), automatización y más.
"""
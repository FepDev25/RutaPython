# Estos operadores devuelven un boolean, dependiendo de la comparaion que le estamos dando.

a = 4
b = 4
resultado = (a == b)
print(f'Resultado == : {resultado}') # True

resultado = (a != b)
print(f'Resultado != : {resultado}') # False

resultado = (a > b)
print(f'Resultado > : {resultado}') # False

resultado = (a >= b)
print(f'Resultado >= : {resultado}') # True

mi_bool = 5 == 5
print(mi_bool)

mi_bool = 2+3 == 5
print(mi_bool)

mi_bool = "Blanco" == "blanco"
print(mi_bool)

mi_bool = 100 == 100.0
print(mi_bool)


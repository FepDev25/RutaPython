# Floats: Acepta valores decimales

mi_numero = 1.5
print(mi_numero)
print(type(mi_numero))

# Declaración de floats
pi = 3.14159
saldo_bancario = 1500.25
temperatura = -10.5

# Operaciones matemáticas
suma = pi + temperatura
resta = saldo_bancario - 100.75
multiplicacion = pi * 2
division = temperatura / 2

print(suma)           # Resultado: -7.35841
print(resta)          # Resultado: 1399.5
print(multiplicacion) # Resultado: 6.28318
print(division)       # Resultado: -5.25
